# This program does basic math.


def main():
    print("8-7={}".format(8 - 7))
    print("5+9={}".format(5 + 9))
    print("10*5={}".format(10 * 5))
    print("7/4={}".format(7 / 4))
    print("6+3*8={}".format(6 + 3 * 8))
    print("7^4={}".format(7 ^ 4))  # Bitwise operation
    print("7**3={}".format(7**3))  # Exponent
    print("14**0.5={}".format(14**0.5))  # Square root


if __name__ == "__main__":
    main()
